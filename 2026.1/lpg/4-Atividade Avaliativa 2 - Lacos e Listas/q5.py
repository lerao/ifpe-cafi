#O que é uma pilha? É dito na questão:
#Em Python, os métodos de lista permitem utilizar listas como pilhas, 
# onde o item adicionado por último é o primeiro a ser recuperado. 
#A pilha é uma estrutura de dados do tipo LIFO (Last In, First Out), ou seja, 
# o último elemento adicionado é o primeiro a ser removido.
#Pense como se estivesse lavando os pratos, após ensaboar você vai empilhando os pratos,
# o primeiro a ser enxaguado será o último que foi colocado ("pilha de pratos")

#Nessa caso, a aquestão pede quais os métodos que adiciona ao final e qual o método que 
#remove do final da pilha (como lista):
# Vimos que:
#append() -> adiciona um item ao final da lista (pilha)
#pop() -> remove o último item da lista (pilha) e o retorna

#Resposta correta: A. append() e pop(). 